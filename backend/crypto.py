def rsa_keygen(bits=512):
    # generate two random primes p and q
    p = _generate_prime(bits // 2)
    q = _generate_prime(bits // 2)

    n = p * q
    phi = (p - 1) * (q - 1)

    # public exponent usually fixed
    e = 65537
    # find d such that d*e = 1 mod phi
    d = pow(e, -1, phi)

    return (e, n), (d, n)

def rsa_encrypt(m, public_key):
    e, n = public_key
    return pow(m, e, n)

def rsa_decrypt(c, private_key):
    d, n = private_key
    return pow(c, d, n)

def _is_prime(num, k=10):
    if num < 2:
        return False
    if num % 2 == 0:
        return num == 2
    # Millerâ€“Rabin primality test
    r, s = 0, num - 1
    while s % 2 == 0:
        r += 1
        s //= 2
    for _ in range(k):
        a = random.randrange(2, num - 1)
        x = pow(a, s, num)
        if x in (1, num - 1):
            continue
        for _ in range(r - 1):
            x = pow(x, 2, num)
            if x == num - 1:
                break
        else:
            return False
    return True

def _generate_prime(bits):
    while True:
        candidate = random.getrandbits(bits) | 1
        if _is_prime(candidate):
            return candidate


