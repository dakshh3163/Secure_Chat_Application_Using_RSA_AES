# Key schedule constants
_rcon = [0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36]


def _sub_word(word):
    return [_sbox[b] for b in word]


def _rot_word(word):
    return word[1:] + word[:1]


def _key_expansion(key):
    # key: 16 bytes
    key_symbols = list(key)
    Nk = 4
    Nr = 10
    Nb = 4

    w = [key_symbols[i*4:i*4+4] for i in range(Nk)]

    for i in range(Nk, Nb*(Nr+1)):
        temp = w[i-1].copy()
        if i % Nk == 0:
            temp = _sub_word(_rot_word(temp))
            temp[0] ^= _rcon[i//Nk - 1]
        w.append([w[i-Nk][j] ^ temp[j] for j in range(4)])
    return w


def _add_round_key(state, w, rnd):
    for r in range(4):
        for c in range(4):
            state[r][c] ^= w[rnd*4 + c][r]


def _sub_bytes(state):
    for r in range(4):
        for c in range(4):
            state[r][c] = _sbox[state[r][c]]


def _shift_rows(state):
    state[1] = state[1][1:] + state[1][:1]
    state[2] = state[2][2:] + state[2][:2]
    state[3] = state[3][3:] + state[3][:3]


def _xtime(a):
    return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1)


def _mix_columns(state):
    for c in range(4):
        a = state[0][c]
        b = state[1][c]
        c0 = state[2][c]
        d = state[3][c]

        state[0][c] = _xtime(a) ^ b ^ _xtime(b) ^ c0 ^ d
        state[1][c] = a ^ _xtime(b) ^ c0 ^ _xtime(c0) ^ d
        state[2][c] = a ^ b ^ _xtime(c0) ^ d ^ _xtime(d)
        state[3][c] = a ^ _xtime(a) ^ b ^ c0 ^ _xtime(d)


def aes_encrypt_block(block16, key16):
    w = _key_expansion(key16)

    state = [list(block16[i:i+4]) for i in range(0, 16, 4)]

    _add_round_key(state, w, 0)

    for rnd in range(1, 10):
        _sub_bytes(state)
        _shift_rows(state)
        _mix_columns(state)
        _add_round_key(state, w, rnd)

    _sub_bytes(state)
    _shift_rows(state)
    _add_round_key(state, w, 10)

    return bytes(sum(state, []))


def aes_encrypt(data_bytes, key16):
    # ECB mode, pad to 16 bytes
    padded = data_bytes + b"\x00" * ((16 - (len(data_bytes) % 16)) % 16)
    out = b""
    for i in range(0, len(padded), 16):
        out += aes_encrypt_block(padded[i:i+16], key16)
    return out
